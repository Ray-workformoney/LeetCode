    /**
     * 先取模 再遍历 时间复杂度O(n)
     * @param chalk
     * @param k
     * @return
     */
    public static int chalkReplacer(int[] chalk, int k) {
        long sum = 0;
        for (int value : chalk) {
            sum += value;
        }
        long c = k % sum;
        int j = 0;
        while (c > 0) {
            c -= chalk[j];
            if (c < 0) {
                break;
            }
            j++;
        }
        return j;
    }

